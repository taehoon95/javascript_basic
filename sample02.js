// splice
let ar = [1, 2, 3];
// [시작위치, 삭제항목수, 추가항목,,,]

ar.splice(1, 0, 10, 20, 30); // 인덱스 1인 위치부터 0개 삭제후 그위치에 10,20,30을 넣는다.

console.log(ar); // [ 1, 10, 20, 30, 2, 3 ]

ar.splice(1, 2, 88, 99);
console.log(ar); //[ 1, 88, 99, 30, 2, 3]

let br = [66, 55, 44, 33];
ar.splice(0, 0, br); //[ [ 66, 55, 44, 33 ], 1, 88, 99, 30, 2, 3 ]
console.log(ar);